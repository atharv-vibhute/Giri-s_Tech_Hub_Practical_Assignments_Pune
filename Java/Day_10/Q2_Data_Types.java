/* 
Q2. Write a Java program to input all basic data types (int, float, double, char, boolean) and print their values. 
Input: 
int = 10 
float = 5.5 
double = 99.99 
char = A 
boolean = true 
Output: 
10 
5.5 
99.99 
A 
true 
Explanation: 
The program accepts values of different data types and displays them. 
*/

public class Q2_Data_Types 
{
	public static void main(String X[]) 
	{
		int i = 10;
		float f = 5.5f;
		double d = 99.99;
		char c = 'A';
		Boolean b = true;

		System.out.println(i);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(b);
	}
}
